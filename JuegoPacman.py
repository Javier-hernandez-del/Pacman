def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')
    
    i = -1
    for point, course in ghosts:
        i += 1
        
        if valid(point + course):
            point.move(course)
        else:
            if (point.x < pacman.x) and (abs(point.x - pacman.x)>abs(point.y - pacman.y)):
                ghosts[i][1] = vector(10,0)
            elif (point.x > pacman.x) and (abs(point.x - pacman.x)>abs(point.y - pacman.y)):
                ghosts[i][1] = vector(-10,0)
            elif (point.y < pacman.y) and (abs(point.x - pacman.x)<abs(point.y - pacman.y)):
                ghosts[i][1] = vector(0,10)
            elif (point.y > pacman.y)and (abs(point.x - pacman.x)<abs(point.y - pacman.y)):
                ghosts[i][1] = vector(0,-10)
            if ghosts[i][1] == course:
                options = []
                if(course.x != 0):
                    options += [vector(0,10),vector(0,-10)]
                else:
                    options += [vector(10,0),vector(-10,0)]
                ghosts[i][1] = choice(options)
        
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

